// YUV 4:2:2 uyvy
void* uyvy_to_422p_2(uchar *input, const int width, const int height)
{
	uchar *dest = (uchar*)malloc( width * height * 3 );
	int i, j;
	uchar *y, *u, *v;

	y = dest;
	v = dest + width * height;
	u = dest + width * height * 3 / 2;

	for (i = 0; i < height; i++)
	{
		for (j = 0; j < width / 2; ++j)
		{
			/* UYVY.  The byte order is CbY'CrY' */
			*u++ = *input++;
			*y++ = *input++;
			*v++ = *input++;
			*y++ = *input++;
		}
	}

	return (void*)dest;
}

