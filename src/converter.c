// YUV 4:2:2 (aka uyvy) to YUV420 (aka yv12)
void*  uyvy_to_yv12(uchar *input, const int width, const int height)
{
	uchar *dest = (uchar*)malloc( width * height * 3 );
	int i, j, w2;
	uchar *y, *u, *v;

	w2 = width / 2;

	/* I420 */
	y = dest;
	v = dest + width * height;
	u = dest + width * height * 5 / 4;

	for (i = 0; i < height; i += 2)
	{
		for (j = 0; j < w2; ++j)
		{
			/* UYVY.  The byte order is CbY'CrY' */
			*u++ = *input++;
			*y++ = *input++;
			*v++ = *input++;
			*y++ = *input++;
		}

		/* down sampling */
		u -= w2;
		v -= w2;

		/* average every second line for U and V */
		for (j = 0; j < w2; ++j)
		{
			int un = *u & 0xff;
			int vn = *v & 0xff; 

			un += *input++ & 0xff;
			*u++ = un>>1;

			*y++ = *input++;

			vn += *input++ & 0xff;
			*v++ = vn>>1;

			*y++ = *input++;
		}
	}

	return (void*)dest;
}

